package day05_Concatenation;

public class StudentInfo {
    public static void main(String[] args) {

        String student_name = "Turkan";
        byte age = 38;
        char gender = 'F';
        String school_name = "Cydeo";
        String phone_number = "(123)-45-6701"; // telefon hem long ile hem string ile yazilabilir
        long phone_number_home = 33577066869503L;
        boolean full_time = true;
        double GPA = 2.5;


        System.out.println("student_name = " + student_name);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("school_name = " + school_name);
        System.out.println("phone_number = " + phone_number);
        System.out.println("phone_number_home = " + phone_number_home);
        System.out.println("full_time = " + full_time);
        System.out.println("GPA = " + GPA);


        /*

        1. student name (String)
    			2. age (int)
    			3. gender (char)
    			4. school name (String)
    			5. phone number (String) (###)-###-####
    			6. full time (boolean)
         */





    }




}
